package PoJo;

public class LocationChild {

	// variables are by default private
	private double lat;
	private double lng;
	
	
	// getters ans setters mehtod
	public double getLat() {
		return lat;
	}
	public void setLat(double lat) {
		this.lat = lat;
	}
	
	
	public double getLng() {
		return lng;
	}
	public void setLng(double lng) {
		this.lng = lng;
	}
	
}
